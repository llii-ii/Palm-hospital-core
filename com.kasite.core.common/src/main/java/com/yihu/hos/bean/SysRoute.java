package com.yihu.hos.bean;

public class SysRoute {
	private String node;
	private String remar;
	private String config;
	private Integer lastmodify;
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public String getRemar() {
		return remar;
	}
	public void setRemar(String remar) {
		this.remar = remar;
	}
	public String getConfig() {
		return config;
	}
	public void setConfig(String config) {
		this.config = config;
	}
	public Integer getLastmodify() {
		return lastmodify;
	}
	public void setLastmodify(Integer lastmodify) {
		this.lastmodify = lastmodify;
	} 
}
